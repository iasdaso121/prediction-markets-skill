#!/usr/bin/env python3
# Part of prediction-markets-skill. Read-only. Stdlib only. Endpoints from docs-raw (see references/).
"""Kalshi order book snapshot for one market.

Fetches GET /markets/{ticker}/orderbook (Trade API v2, unauthenticated) and
prints one JSON document to stdout: the raw book (venue-native fixed-point
dollar/count STRINGS, passed through unchanged) plus a computed summary.

Kalshi books contain BIDS ONLY, on both sides. Asks are derived by complement
(references/market-mechanics.md section 7):
    best_yes_ask = 1.00 - best_no_bid
    best_no_ask  = 1.00 - best_yes_bid
"""

import argparse
import json
import sys
import time
import urllib.error
import urllib.parse
import urllib.request
from datetime import datetime, timezone

# Base URLs from references/kalshi-api.md (spec v3.23.0, docs-raw fetched 2026-07-01).
BASE_URL = "https://external-api.kalshi.com/trade-api/v2"
PRIMARY_HOST = urllib.parse.urlsplit(BASE_URL).hostname
# Documented shared prod host (same Base URLs table). Used ONLY as a read-only
# classification probe: external-api region-blocks with a BARE nginx "403 Forbidden"
# (no wording at all), while this host's CloudFront block page says why.
GEO_PROBE_HOST = "api.elections.kalshi.com"
USER_AGENT = "prediction-markets-skill/0.1 (+https://github.com/iasdaso121/prediction-markets-skill)"
RETRY_BACKOFF_S = (1, 2, 4)
EXIT_CODES = {"usage": 2, "network": 3, "rate-limit": 4, "geo": 5, "not-found": 6, "schema": 7}
# Kalshi fronts with Amazon CloudFront, not Cloudflare. Real block bodies (verified
# live 2026-07-02): api.elections -> "...configured to block access from your
# country... Generated by cloudfront"; demo host -> "Request blocked."
GEO_WORDS = ("cloudfront", "country", "block access", "request blocked",
             "cloudflare", "region", "blocked", "unavailable in your", "not available in your")

EPILOG = """\
examples:
  # Full book for an NYC daily-high weather market. Daily tickers roll over:
  # list today's via GET /markets?series_ticker=KXHIGHNY&status=open
  kalshi_orderbook.py KXHIGHNY-26JUL02-B99.5

  # Only the best 5 levels per side (server-side trim via the depth param)
  kalshi_orderbook.py KXHIGHNY-26JUL02-T99 --depth 5

  # Slow network: raise the per-request timeout
  kalshi_orderbook.py KXHIGHNY-26JUL02-B101.5 --depth 10 --timeout 30
"""


def fail(category, message, hint=""):
    """Print the shared one-line JSON error to stderr and exit with the mapped code."""
    print(json.dumps({"error": {"category": category, "message": message, "hint": hint}}),
          file=sys.stderr)
    sys.exit(EXIT_CODES[category])


class JsonErrorParser(argparse.ArgumentParser):
    """argparse that reports bad usage as the shared JSON error line (exit 2)."""

    def error(self, message):
        print(self.format_usage().strip(), file=sys.stderr)
        fail("usage", message, "run with --help for examples")


def looks_geo_blocked(body):
    lowered = body.lower()
    return any(word in lowered for word in GEO_WORDS)


def probe_shared_host(url, timeout):
    """Re-GET the same path/query on the shared prod host to classify a bare 403.

    Returns "geo" (shared host shows a geo-block page), "ok" (shared host serves
    the request, so the bare 403 was not a region block), or "unknown".
    One extra request, only on the already-failed 403 path -- never on success.
    """
    parts = urllib.parse.urlsplit(url)
    probe_url = urllib.parse.urlunsplit(
        (parts.scheme, GEO_PROBE_HOST, parts.path, parts.query, ""))
    request = urllib.request.Request(
        probe_url, headers={"User-Agent": USER_AGENT, "Accept": "application/json"}, method="GET")
    try:
        with urllib.request.urlopen(request, timeout=timeout):
            return "ok"
    except urllib.error.HTTPError as exc:
        body = exc.read().decode("utf-8", "replace")
        if exc.code in (403, 451) and looks_geo_blocked(body):
            return "geo"
        return "unknown"
    except (urllib.error.URLError, TimeoutError, OSError):
        return "unknown"


def http_get_json(url, timeout):
    """GET url; retry up to 3 times on 429/5xx with 1s/2s/4s backoff, honoring Retry-After."""
    host = urllib.parse.urlsplit(url).hostname or "kalshi"
    path = urllib.parse.urlsplit(url).path
    last_status = None
    for attempt in range(len(RETRY_BACKOFF_S) + 1):
        request = urllib.request.Request(
            url, headers={"User-Agent": USER_AGENT, "Accept": "application/json"}, method="GET")
        try:
            with urllib.request.urlopen(request, timeout=timeout) as response:
                body = response.read().decode("utf-8", "replace")
            try:
                return json.loads(body)
            except json.JSONDecodeError as exc:
                fail("schema", f"non-JSON 200 response from {host}: {exc}",
                     "API contract may have changed; re-fetch docs-raw/ and re-check")
        except urllib.error.HTTPError as exc:
            status = exc.code
            body = exc.read().decode("utf-8", "replace")
            if status == 404:
                fail("not-found", f"404 from {host}{path}",
                     "check the market ticker (daily tickers roll over; list open markets "
                     "for the series to find current ones)")
            if status in (403, 451):
                if looks_geo_blocked(body):
                    fail("geo", f"HTTP {status} geo-block from {host}",
                         f"Kalshi appears blocked from your region; check your region/VPN for {host}")
                # external-api region-blocks with a bare "403 Forbidden" (no wording,
                # verified live 2026-07-02); confirm via the shared host before deciding.
                verdict = probe_shared_host(url, timeout) if host == PRIMARY_HOST else "unknown"
                if verdict == "geo":
                    fail("geo",
                         f"HTTP {status} from {host} is a region block "
                         f"({GEO_PROBE_HOST} returned a CloudFront country-block page)",
                         "Kalshi is blocked from your region; check your region/VPN")
                hint = ("access denied but no geo-block wording; check the request"
                        if verdict == "ok" else
                        "no geo-block wording, but Kalshi region blocks on this host can be "
                        "a bare 403; check your region/VPN or the request")
                fail("network", f"HTTP {status} from {host}: {body[:200]}", hint)
            if status == 429 or 500 <= status < 600:
                last_status = status
                if attempt < len(RETRY_BACKOFF_S):
                    delay = RETRY_BACKOFF_S[attempt]
                    retry_after = (exc.headers.get("Retry-After") or "").strip()
                    # Integer-seconds form only: Kalshi 429s carry no Retry-After at all
                    # (references/gotchas.md rate-limit entry), and the HTTP-date form
                    # would need parsers outside this skill's approved stdlib subset.
                    if retry_after.isdigit():
                        delay = max(delay, int(retry_after))
                    print(f"HTTP {status} from {host}; retrying in {delay}s "
                          f"(attempt {attempt + 2}/{len(RETRY_BACKOFF_S) + 1})", file=sys.stderr)
                    time.sleep(delay)
                    continue
                break
            fail("network", f"HTTP {status} from {host}: {body[:200]}", "unexpected status code")
        except (urllib.error.URLError, TimeoutError, OSError) as exc:
            fail("network", f"request to {host} failed: {exc}",
                 "check connectivity/DNS or raise --timeout")
    if last_status == 429:
        fail("rate-limit",
             f"still rate-limited by {host} after {len(RETRY_BACKOFF_S) + 1} attempts",
             "Kalshi sends no Retry-After header on 429; wait longer and retry")
    fail("network",
         f"HTTP {last_status} from {host} persisted after {len(RETRY_BACKOFF_S) + 1} attempts",
         "server error did not clear; retry later")


def parse_levels(raw_levels, side_name):
    """Validate one bid side ([[price_dollars, count_fp], ...] string pairs) -> [(float, float)]."""
    if not isinstance(raw_levels, list):
        fail("schema", f"orderbook_fp.{side_name} is not a list",
             "expected arrays of [price_dollars, count_fp] per docs-raw/kalshi/openapi.yaml")
    parsed = []
    for level in raw_levels:
        try:
            parsed.append((float(level[0]), float(level[1])))
        except (TypeError, ValueError, IndexError):
            fail("schema", f"unexpected level shape in {side_name}: {level!r}",
                 "expected a [price_dollars, count_fp] string pair")
    return parsed


def round4(value):
    return None if value is None else round(value, 4)


def summarize(yes_levels, no_levels):
    """Best bid/ask, spread, midpoint, implied P(YES), and per-side depth.

    Ordering caveat: the OpenAPI prose says levels run best-to-worst, while
    references/gotchas.md (live-verified) says ascending with best bid LAST.
    We take max(price) per side, which is correct under either ordering.
    Asks derive from opposite-side bids at $1.00 complement (documented).
    """
    best_yes_bid = max((price for price, _ in yes_levels), default=None)
    best_no_bid = max((price for price, _ in no_levels), default=None)
    best_yes_ask = None if best_no_bid is None else 1.0 - best_no_bid
    spread = midpoint = None
    if best_yes_bid is not None and best_yes_ask is not None:
        spread = best_yes_ask - best_yes_bid
        midpoint = (best_yes_bid + best_yes_ask) / 2
    return {
        "best_yes_bid": round4(best_yes_bid),
        "best_yes_ask": round4(best_yes_ask),
        "spread": round4(spread),
        "midpoint": round4(midpoint),
        "implied_probability": round4(midpoint),  # midpoint of a $1-payout binary = P(YES)
        "yes_depth_contracts": round4(sum(count for _, count in yes_levels)),
        "no_depth_contracts": round4(sum(count for _, count in no_levels)),
    }


def main():
    parser = JsonErrorParser(
        description="Fetch the Kalshi order book for one market (read-only, no auth) and "
                    "print raw levels plus a computed bid/ask summary as JSON.",
        epilog=EPILOG,
        formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("ticker", help="Kalshi market ticker, e.g. KXHIGHNY-26JUL02-B99.5")
    parser.add_argument("--depth", type=int, default=None, metavar="N",
                        help="levels per side to return, 0-100 (server-side trim; "
                             "0 or omitted = all levels)")
    parser.add_argument("--timeout", type=float, default=15, metavar="SECONDS",
                        help="per-request timeout in seconds (default: 15)")
    args = parser.parse_args()

    ticker = args.ticker.strip()
    if not ticker:
        fail("usage", "market ticker must be non-empty",
             "pass a market ticker like KXHIGHNY-26JUL02-B99.5")
    # Spec note: openapi.yaml prose says "0 or negative means all levels", but the
    # schema itself is minimum:0/maximum:100 (validate: omitempty,min=0,max=100).
    # We follow the schema and reject negatives (exit 2); use 0 for "all levels".
    if args.depth is not None and not 0 <= args.depth <= 100:
        fail("usage", f"--depth must be between 0 and 100 (got {args.depth})",
             "0 (or omitting --depth) returns all levels")

    params = {"ticker": ticker}
    query = ""
    if args.depth:  # depth=0 means "all levels" server-side, same as omitting the param
        params["depth"] = args.depth
        query = "?" + urllib.parse.urlencode({"depth": args.depth})
    endpoint = f"/markets/{urllib.parse.quote(ticker, safe='')}/orderbook"
    # NB: the OpenAPI spec formally lists auth on this endpoint, but prose docs and
    # live checks confirm unauthenticated GET works (references/kalshi-api.md).
    payload = http_get_json(BASE_URL + endpoint + query, args.timeout)

    book = payload.get("orderbook_fp")
    if not isinstance(book, dict):
        fail("schema", "response is missing the orderbook_fp object",
             "expected {'orderbook_fp': {...}} per docs-raw/kalshi/openapi.yaml")
    yes_raw = book.get("yes_dollars") or []  # empty book sides can come back null
    no_raw = book.get("no_dollars") or []
    if not yes_raw and not no_raw:
        # Live-checked 2026-07-02: the orderbook endpoint returns HTTP 200 with an empty
        # book for NONEXISTENT tickers (no 404). Disambiguate via GET /markets/{ticker},
        # which does 404 on unknown tickers. Costs one extra request, empty-book case only.
        check = f"/markets/{urllib.parse.quote(ticker, safe='')}"
        http_get_json(BASE_URL + check, args.timeout)  # 404 inside -> exit 6
        print(f"note: {ticker} exists but its orderbook is empty", file=sys.stderr)
    yes_levels = parse_levels(yes_raw, "yes_dollars")
    no_levels = parse_levels(no_raw, "no_dollars")

    envelope = {
        "venue": "kalshi",
        "endpoint": endpoint,
        "params": params,
        "fetched_at": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "count": len(yes_levels) + len(no_levels),
        "data": {
            "ticker": ticker,
            # Venue-native strings passed through unchanged (null sides -> []).
            "orderbook_fp": {"yes_dollars": yes_raw, "no_dollars": no_raw},
            "summary": summarize(yes_levels, no_levels),
        },
    }
    print(json.dumps(envelope, indent=2))


if __name__ == "__main__":
    main()
