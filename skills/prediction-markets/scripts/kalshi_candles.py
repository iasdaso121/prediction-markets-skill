#!/usr/bin/env python3
# Part of prediction-markets-skill. Read-only. Stdlib only. Endpoints from docs-raw (see references/).
"""Fetch candlestick (OHLC) price history for one Kalshi market.

Endpoint: GET /series/{series_ticker}/markets/{ticker}/candlesticks
(requires BOTH tickers — see references/gotchas.md). If --series is omitted
the script resolves it with extra GETs: /markets/{ticker} -> event_ticker,
then /events/{event_ticker} -> series_ticker. Per docs-raw/kalshi/openapi.yaml
the Market object carries only event_ticker (no series field), so resolution
costs TWO extra requests, not one.

Geo-blocks: Kalshi's primary host can reject blocked regions with a bare
keyword-less 403 (awselb); on such a 403 the script probes the secondary
documented host once to classify it (see sniff_alt_host).
"""

import argparse
import json
import sys
import time
import urllib.error
import urllib.parse
import urllib.request
from datetime import datetime, timezone

BASE = "https://external-api.kalshi.com/trade-api/v2"  # primary prod host, references/kalshi-api.md
ALT_BASE = "https://api.elections.kalshi.com/trade-api/v2"  # secondary documented prod host, gotchas.md "Base URLs"
USER_AGENT = "prediction-markets-skill/0.1 (+https://github.com/iasdaso121/prediction-markets-skill)"
PERIOD_CHOICES = (1, 60, 1440)  # minutes; validated enum per openapi.yaml
EXIT_CODES = {"usage": 2, "network": 3, "rate-limit": 4, "geo": 5, "not-found": 6, "schema": 7}


def log(msg):
    print(msg, file=sys.stderr)


def die(category, message, hint=""):
    err = {"error": {"category": category, "message": message, "hint": hint}}
    print(json.dumps(err), file=sys.stderr)
    sys.exit(EXIT_CODES[category])


def looks_geo_blocked(body):
    # Live API disagrees with generic CDN wording (observed 2026-07-02, per the
    # "live disagrees with docs" rule): api.elections.kalshi.com geo-blocks via
    # CloudFront with "...configured to block access from your country ...
    # Generated by cloudfront"; external-api.kalshi.com (awselb) geo-blocks with
    # a bare "403 Forbidden" and NO wording at all — that case is handled by
    # sniff_alt_host() below, since no body sniff can catch it.
    lowered = body.lower()
    return any(word in lowered for word in
               ("cloudflare", "cloudfront", "region", "country", "block", "restricted", "not available in your"))


def sniff_alt_host(path, params, timeout, code):
    """One probe GET to ALT_BASE after a wordless 403/451 from the primary host.

    Observed live 2026-07-02: a geo-blocked region gets a keyword-less awselb
    "403 Forbidden" from external-api.kalshi.com but an explicit CloudFront
    country-block page from api.elections.kalshi.com. Sniffing the secondary
    host's body is the only way to classify the wordless primary 403.
    Dies (category geo, exit 5) if the probe body shows block wording; returns
    parsed JSON (and switches BASE) if the secondary host actually works;
    returns None when still ambiguous so the caller reports network.
    """
    global BASE
    url = ALT_BASE + path
    if params:
        url += "?" + urllib.parse.urlencode(params)
    alt_host = urllib.parse.urlsplit(ALT_BASE).netloc
    log("HTTP %d with no block wording from primary host — probing %s once to distinguish geo-block from other errors" % (code, alt_host))
    req = urllib.request.Request(url, headers={"User-Agent": USER_AGENT, "Accept": "application/json"})
    try:
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            alt_body = resp.read().decode("utf-8", "replace")
    except urllib.error.HTTPError as alt_exc:
        alt_body = alt_exc.read().decode("utf-8", "replace")
        if alt_exc.code in (403, 451) and looks_geo_blocked(alt_body):
            die("geo", "HTTP %d from both documented Kalshi hosts; %s says: %s" % (code, alt_host, alt_body[:200]),
                "Kalshi geo-blocks some regions at the CDN edge; check your region/VPN.")
        return None
    except (urllib.error.URLError, TimeoutError, OSError):
        return None
    try:
        parsed = json.loads(alt_body)
    except ValueError:
        return None
    log("Secondary host %s answered OK — using it for the rest of this run" % alt_host)
    BASE = ALT_BASE
    return parsed


def http_get_json(path, params, timeout):
    """GET BASE+path, retrying up to 3 times on 429/5xx (backoff 1s/2s/4s)."""
    url = BASE + path
    if params:
        url += "?" + urllib.parse.urlencode(params)
    backoffs = [1, 2, 4]
    last_status = None
    for attempt in range(len(backoffs) + 1):
        req = urllib.request.Request(url, headers={"User-Agent": USER_AGENT, "Accept": "application/json"})
        try:
            with urllib.request.urlopen(req, timeout=timeout) as resp:
                body = resp.read().decode("utf-8", "replace")
            try:
                return json.loads(body)
            except ValueError:
                die("schema", "Response from %s is not valid JSON" % url, "Re-fetch docs-raw and re-check the endpoint.")
        except urllib.error.HTTPError as exc:
            body = exc.read().decode("utf-8", "replace")
            last_status = exc.code
            if exc.code in (403, 451):
                if looks_geo_blocked(body):
                    die("geo", "HTTP %d from %s — host appears geo-blocked: %s" % (exc.code, urllib.parse.urlsplit(url).netloc, body[:200]),
                        "Kalshi restricts some regions; check your region/VPN.")
                if BASE != ALT_BASE:
                    parsed = sniff_alt_host(path, params, timeout, exc.code)
                    if parsed is not None:
                        return parsed
                die("network", "HTTP %d from %s: %s" % (exc.code, url, body[:200]),
                    "No block wording, but Kalshi's edge (awselb) is known to geo-block with a bare 403 — check your region/VPN before assuming a network fault.")
            if exc.code == 404:
                die("not-found", "HTTP 404 from %s: %s" % (url, body[:200]),
                    "Check the market/series ticker; settled markets older than the cutoff live under /historical/.")
            if exc.code == 429 or exc.code >= 500:
                if attempt < len(backoffs):
                    wait = backoffs[attempt]
                    retry_after = exc.headers.get("Retry-After")
                    if retry_after:
                        try:
                            wait = max(wait, int(retry_after))
                        except ValueError:
                            pass
                    log("HTTP %d from %s — retry %d/%d in %ds" % (exc.code, url, attempt + 1, len(backoffs), wait))
                    time.sleep(wait)
                    continue
                if exc.code == 429:
                    die("rate-limit", "Still rate-limited (429) after retries: %s" % url,
                        "Kalshi sends no Retry-After header; slow down and try again later.")
                die("network", "HTTP %d from %s after retries: %s" % (exc.code, url, body[:200]), "Server error persisted.")
            die("network", "HTTP %d from %s: %s" % (exc.code, url, body[:200]), "Unexpected status code.")
        except (urllib.error.URLError, TimeoutError, OSError) as exc:
            if attempt < len(backoffs):
                log("Network error (%s) — retry %d/%d in %ds" % (exc, attempt + 1, len(backoffs), backoffs[attempt]))
                time.sleep(backoffs[attempt])
                continue
            die("network", "Failed to reach %s: %s" % (url, exc), "Check connectivity / --timeout.")
    die("network", "Exhausted retries for %s (last status %s)" % (url, last_status), "")


def parse_when(value, label):
    """Accept unix seconds ('1751400000') or ISO8601 date/datetime; return unix seconds (UTC)."""
    value = value.strip()
    if value.lstrip("-").isdigit():
        ts = int(value)
        # Guard against typos: "--start 2026" (a bare year -> Jan 1 1970) or a
        # negative value would otherwise flow through to the API silently.
        # 10^9 = 2001-09-09; Kalshi has no data anywhere near that early.
        if ts < 1_000_000_000:
            die("usage", "%s value %r doesn't look like unix seconds" % (label, value),
                "Pass unix seconds (10 digits, e.g. 1751400000), YYYY-MM-DD, or an ISO8601 datetime.")
        return ts
    try:
        dt = datetime.fromisoformat(value.replace("Z", "+00:00"))
    except ValueError:
        die("usage", "Cannot parse %s value %r" % (label, value), "Use unix seconds, YYYY-MM-DD, or ISO8601 datetime.")
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=timezone.utc)  # naive input treated as UTC
    return int(dt.timestamp())


def resolve_series(ticker, timeout):
    """Two extra GETs: market -> event_ticker, event -> series_ticker (Market has no series field)."""
    log("--series not given; resolving via GET /markets/%s then GET /events/{event_ticker}" % ticker)
    market = http_get_json("/markets/%s" % urllib.parse.quote(ticker), None, timeout).get("market") or {}
    event_ticker = market.get("event_ticker")
    if not event_ticker:
        die("schema", "Market %s has no event_ticker field" % ticker, "Response shape changed? Re-check docs-raw.")
    event = http_get_json("/events/%s" % urllib.parse.quote(event_ticker), None, timeout).get("event") or {}
    series = event.get("series_ticker")
    if not series:
        die("schema", "Event %s has no series_ticker field" % event_ticker, "Response shape changed? Re-check docs-raw.")
    log("Resolved series_ticker=%s (event %s)" % (series, event_ticker))
    return series


def implied_probability(price):
    """Float from price close (last trade of the period), falling back to previous_dollars."""
    if not isinstance(price, dict):
        return None
    raw = price.get("close_dollars")
    if raw is None:
        raw = price.get("previous_dollars")
    if raw is None:
        return None
    try:
        return round(float(raw), 4)
    except (TypeError, ValueError):
        return None


def shape_candle(raw):
    ts = raw.get("end_period_ts")
    iso = None
    if isinstance(ts, (int, float)):
        iso = datetime.fromtimestamp(int(ts), timezone.utc).isoformat().replace("+00:00", "Z")
    return {
        "end_period_ts": ts,
        "end_period_iso": iso,
        "yes_bid": raw.get("yes_bid"),      # {open,low,high,close}_dollars strings — passthrough
        "yes_ask": raw.get("yes_ask"),      # same nested dollar-string OHLC — passthrough
        "price": raw.get("price"),          # trade OHLC + mean/previous_dollars, nullable — passthrough
        "volume_fp": raw.get("volume_fp"),
        "open_interest_fp": raw.get("open_interest_fp"),
        "implied_probability": implied_probability(raw.get("price")),
    }


class JsonErrorParser(argparse.ArgumentParser):
    """argparse that reports bad usage as the shared JSON error line (exit 2)."""

    def error(self, message):
        print(self.format_usage().strip(), file=sys.stderr)
        die("usage", message, "run with --help for examples")


def main():
    parser = JsonErrorParser(
        description="Kalshi candlestick (OHLC) price history for one market. Read-only, no auth.",
        epilog="""examples:
  %(prog)s KXHIGHNY-26JUL02-T99 --series KXHIGHNY --period 60 --limit 24
  %(prog)s KXHIGHNY-26JUL02-B99.5 --period 1 --start 2026-07-02 --end 2026-07-02T12:00:00Z
  %(prog)s KXHIGHNY-26JUL02-T106 --series KXHIGHNY --period 1440 --start 1750982400 --end 1751500800

Prices are Kalshi fixed-point dollar STRINGS (e.g. "0.4200"); implied_probability is a computed float.""",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("ticker", help="Market ticker, e.g. KXHIGHNY-26JUL02-T99")
    parser.add_argument("--series", help="Series ticker (e.g. KXHIGHNY). Omit to auto-resolve (costs 2 extra GETs).")
    parser.add_argument("--start", help="Range start: unix seconds, YYYY-MM-DD, or ISO8601. Default: end - limit*period.")
    parser.add_argument("--end", help="Range end: unix seconds, YYYY-MM-DD, or ISO8601. Default: now.")
    parser.add_argument("--period", type=int, choices=PERIOD_CHOICES, default=60,
                        help="Candle length in minutes: 1, 60, or 1440 (spec enum). Default 60.")
    parser.add_argument("--limit", type=int, default=100, help="Max candles returned (most recent kept). Default 100.")
    parser.add_argument("--timeout", type=int, default=15, help="Per-request timeout in seconds. Default 15.")
    args = parser.parse_args()

    if args.limit < 1:
        die("usage", "--limit must be >= 1", "")
    if args.timeout < 1:
        die("usage", "--timeout must be >= 1", "")

    end_ts = parse_when(args.end, "--end") if args.end else int(time.time())
    start_ts = parse_when(args.start, "--start") if args.start else end_ts - args.limit * args.period * 60
    if start_ts >= end_ts:
        die("usage", "--start (%d) must be before --end (%d)" % (start_ts, end_ts), "")

    series = args.series or resolve_series(args.ticker, args.timeout)

    path = "/series/%s/markets/%s/candlesticks" % (urllib.parse.quote(series), urllib.parse.quote(args.ticker))
    query = {"start_ts": start_ts, "end_ts": end_ts, "period_interval": args.period}
    payload = http_get_json(path, query, args.timeout)

    candles = payload.get("candlesticks")
    if not isinstance(candles, list):
        die("schema", "No 'candlesticks' array in response from %s" % path, "Response shape changed? Re-check docs-raw.")
    candles = sorted(candles, key=lambda c: c.get("end_period_ts") or 0)
    if len(candles) > args.limit:
        log("Trimming %d candles to the most recent --limit %d" % (len(candles), args.limit))
        candles = candles[-args.limit:]

    data = [shape_candle(c) for c in candles]
    result = {
        "venue": "kalshi",
        "endpoint": path,
        "params": {"series_ticker": series, "ticker": payload.get("ticker", args.ticker),
                   "start_ts": start_ts, "end_ts": end_ts, "period_interval": args.period},
        "fetched_at": datetime.now(timezone.utc).isoformat(timespec="seconds").replace("+00:00", "Z"),
        "count": len(data),
        "data": data,
    }
    json.dump(result, sys.stdout, indent=2)
    sys.stdout.write("\n")


if __name__ == "__main__":
    main()
